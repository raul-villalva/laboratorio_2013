package primero;

public class EstudianteSecundario extends Estudiante{
	
	private int materia_aprobadas;
	private double promedio;
	public int getMateria_aprobadas() {
		return materia_aprobadas;
	}
	public void setMateria_aprobadas(int materia_aprobadas) {
		this.materia_aprobadas = materia_aprobadas;
	}
	public double getPromedio() {
		return promedio;
	}
	public void setPromedio(double promedio) {
		this.promedio = promedio;
	}

}
